/* 
 Un array en PHP es en realidad un mapa ordenado. 
 * Un mapa es un tipo de datos que asocia valores con claves. 
 * Este tipo se optimiza para varios usos diferentes; se puede emplear como un array. 
 * ista (vector), tabla asociativa (tabla hash - una implementación de un mapa), diccionario, colección, pila, cola, y 
 * posiblemente más. Ya que los valores de un array pueden ser otros arrays, también son posibles árboles y 
 * arrays multidimensionales.
 */
//Example: 
$array = array(
    "foo" => "bar",
    "bar" => "foo",
);

$array = array("foo", "bar", "hello", "world");
